#!/bin/bash
# 🔥 БЫСТРЫЙ СТАРТ - Всё за один скрипт

echo "═══════════════════════════════════════════════════════════════"
echo "🚀 SNOSER-TG БЫСТРЫЙ СТАРТ"
echo "═══════════════════════════════════════════════════════════════"
echo ""

# Проверяем venv
if [ ! -d ".venv" ]; then
    echo "⚠️  Виртуальное окружение не найдено!"
    echo "Создаю .venv..."
    python3 -m venv .venv
fi

# Активируем venv
source .venv/bin/activate

echo "✅ Окружение активировано"
echo ""

# Меню
echo "┌──────────────────────────────────────────────────────────────┐"
echo "│  ВЫБЕРИ ЧТО СДЕЛАТЬ:                                         │"
echo "├──────────────────────────────────────────────────────────────┤"
echo "│  1️⃣  - Генерировать 1200 отправителей                       │"
echo "│  2️⃣  - Настроить параметры отправки (350+)                  │"
echo "│  3️⃣  - Добавить прокси                                       │"
echo "│  4️⃣  - Запустить отправку (пользователь)                    │"
echo "│  5️⃣  - Запустить отправку (канал)                           │"
echo "│  6️⃣  - Полная настройка (1+2+3)                             │"
echo "│  0️⃣  - Выход                                                │"
echo "└──────────────────────────────────────────────────────────────┘"
echo ""

read -p "Выбирай номер: " choice

case $choice in
    1)
        echo "⏳ Генерирую 1200 отправителей..."
        python3 ukraine_senders_1200.py
        echo "✅ Готово!"
        ;;
    2)
        echo "⏳ Открываю настройки параметров..."
        python3 sending_config.py setup
        echo "✅ Готово!"
        ;;
    3)
        echo "⏳ Открываю добавление прокси..."
        python3 proxy_generator.py add
        echo "✅ Готово!"
        ;;
    4)
        echo "🚀 Запускаю отправку на пользователя..."
        python3 NewLinks.py
        ;;
    5)
        echo "🚀 Запускаю отправку на канал..."
        python3 NewLinks.py
        ;;
    6)
        echo "⏳ ПОЛНАЯ НАСТРОЙКА"
        echo ""
        echo "📌 ШАГ 1: Генерирую 1200 отправителей..."
        python3 ukraine_senders_1200.py
        echo ""
        echo "📌 ШАГ 2: Настраиваю параметры (350+)..."
        python3 sending_config.py setup
        echo ""
        echo "📌 ШАГ 3: Добавляю прокси..."
        python3 proxy_generator.py add
        echo ""
        echo "✅ ПОЛНАЯ НАСТРОЙКА ЗАВЕРШЕНА!"
        echo "Теперь выбери пункт 4 или 5 для запуска отправки"
        ;;
    0)
        echo "Выход..."
        exit 0
        ;;
    *)
        echo "❌ Неправильный выбор"
        exit 1
        ;;
esac

echo ""
echo "═══════════════════════════════════════════════════════════════"
